-- ============================================================
-- master-skills Migration A
-- マスタ 3 テーブル (master_trade_types / master_qualifications / master_skill_tags)
-- の作成 + RLS + 部分インデックス + 初期データ投入
--
-- 設計判断:
--   - 階層・親子・カテゴリ情報は持たず、trade-types の階層は label 内 prefix
--     ("大カテ/中カテ｜末端") のみで表現する
--   - マスタ参照は anon + authenticated とも SELECT 可（公開読取）。
--     INSERT/UPDATE/DELETE は service_role のみ（Supabase ダッシュボード経由運用）
--   - 廃止 (deprecated_at IS NOT NULL) は「新規選択からは除外、既存保有は保持」
--   - 保存方式は denormalization（ユーザー側カラムに label をコピー保存、FK 化しない）
--
-- 関連 spec: .kiro/specs/master-skills/{requirements,design}.md
-- ============================================================

-- ============================================================
-- 1. テーブル作成
-- ============================================================

CREATE TABLE master_trade_types (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  label text NOT NULL UNIQUE,
  deprecated_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE master_qualifications (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  label text NOT NULL UNIQUE,
  deprecated_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE master_skill_tags (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  label text NOT NULL UNIQUE,
  deprecated_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- ============================================================
-- 2. updated_at トリガー
-- ============================================================

CREATE TRIGGER trigger_master_trade_types_updated_at
  BEFORE UPDATE ON master_trade_types
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

CREATE TRIGGER trigger_master_qualifications_updated_at
  BEFORE UPDATE ON master_qualifications
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

CREATE TRIGGER trigger_master_skill_tags_updated_at
  BEFORE UPDATE ON master_skill_tags
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ============================================================
-- 3. 部分 B-tree インデックス (label, WHERE deprecated_at IS NULL)
--    active マスタの label 検索を高速化
-- ============================================================

CREATE INDEX idx_master_trade_types_label_active
  ON master_trade_types (label)
  WHERE deprecated_at IS NULL;

CREATE INDEX idx_master_qualifications_label_active
  ON master_qualifications (label)
  WHERE deprecated_at IS NULL;

CREATE INDEX idx_master_skill_tags_label_active
  ON master_skill_tags (label)
  WHERE deprecated_at IS NULL;

-- ============================================================
-- 4. RLS: anon + authenticated は SELECT 可、書き込みは service_role のみ
--    (書き込みポリシー未定義 = デフォルト拒否、service_role は RLS バイパス)
-- ============================================================

ALTER TABLE master_trade_types ENABLE ROW LEVEL SECURITY;
ALTER TABLE master_qualifications ENABLE ROW LEVEL SECURITY;
ALTER TABLE master_skill_tags ENABLE ROW LEVEL SECURITY;

CREATE POLICY master_trade_types_select_all_anon
  ON master_trade_types
  FOR SELECT
  TO anon, authenticated
  USING (true);

CREATE POLICY master_qualifications_select_all_anon
  ON master_qualifications
  FOR SELECT
  TO anon, authenticated
  USING (true);

CREATE POLICY master_skill_tags_select_all_anon
  ON master_skill_tags
  FOR SELECT
  TO anon, authenticated
  USING (true);

